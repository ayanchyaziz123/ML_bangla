    <pre><code>import networkx as nx
import numpy as np
import matplotlib.pyplot as plt

# গ্রাফ তৈরি করো
G = nx.Graph()

# নোড যোগ করো (ব্যক্তির নাম)
users = ['রাহেলা', 'করিম', 'সুমাইয়া', 'জামাল', 'নাফিসা', 'তারেক']
G.add_nodes_from(users)

# এজ যোগ করো (বন্ধুত্ব)
friendships = [
    ('রাহেলা', 'করিম'),
    ('রাহেলা', 'সুমাইয়া'),
    ('করিম', 'জামাল'),
    ('সুমাইয়া', 'নাফিসা'),
    ('জামাল', 'নাফিসা'),
    ('নাফিসা', 'তারেক'),
    ('করিম', 'নাফিসা'),
]
G.add_edges_from(friendships)

# গ্রাফের বেসিক তথ্য
print(f"নোড সংখ্যা: {G.number_of_nodes()}")
print(f"এজ সংখ্যা: {G.number_of_edges()}")
print(f"প্রতিটি নোডের ডিগ্রি:")
for node, degree in G.degree():
    print(f"  {node}: {degree}")

# গ্রাফ ভিজুয়ালাইজ
plt.figure(figsize=(8, 6))
pos = nx.spring_layout(G, seed=42)
nx.draw(G, pos,
        with_labels=True,
        node_color='lightblue',
        node_size=1500,
        font_size=10,
        font_weight='bold',
        edge_color='gray',
        width=2)
plt.title("সোশ্যাল নেটওয়ার্ক গ্রাফ")
plt.tight_layout()
plt.savefig('social_network.png', dpi=150)
plt.show()</code></pre>
